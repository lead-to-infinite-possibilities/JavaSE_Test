package com.heima.operator;
/**
  + 连接符
 */
public class demo2 {
    public static void main(String[] args) {
        int a1 = 100;
        System.out.println("abc"+a1);
        System.out.println("abc"+a1+100);
        System.out.println("abc"+a1+"def");
        System.out.println('a'+a1+"abc");
        System.out.println("abc"+a1+'a');
        System.out.println('a'+10.5);
    }
}
