package com.heima.operator;
/**
 自增/自减运算符 ++/--
 */
public class demo3 {
    public static void main(String[] args) {
        int a = 10;
        a++;
        System.out.println(a);
        a--;
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(1+a++);
    }
}
