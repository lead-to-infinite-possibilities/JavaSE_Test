package com.heima.operator;
/**
 关系运算符
 */
public class demo6 {
    public static void main(String[] args) {
        int a = 10,b = 5;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}
