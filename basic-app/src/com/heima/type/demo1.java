package com.heima.type;
/**
    类型转换
 */
public class demo1 {
    public static void main(String[] args) {
        byte a = 123;
        int b = a;
        char c = 'a';
        int d = c;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
